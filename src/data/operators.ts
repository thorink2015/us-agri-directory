export function getOperatorBySlug(slug: string): Operator | undefined {
  return operators.find((op) => op.slug === slug);
}

export function getOperatorsByCounty(stateSlug: string): Operator[] {
  return operators
    .filter((op) => op.counties.includes(stateSlug))
    .sort((a, b) => {
      const aFeat = a.featured ? 1 : 0;
      const bFeat = b.featured ? 1 : 0;
      if (aFeat !== bFeat) return bFeat - aFeat;
      const aVer = a.verified && !a.pendingConfirmation ? 1 : 0;
      const bVer = b.verified && !b.pendingConfirmation ? 1 : 0;
      if (aVer !== bVer) return bVer - aVer;
      return 0;
    });
}

export function getFeaturedOperators(): Operator[] {
  return operators.filter((op) => op.featured);
}

export function getVerifiedOperators(): Operator[] {
  return operators.filter((op) => op.verified);
}
